# Separando os tempos de chegada e de serviço a partir dos dados
tempos_de_servico = [t[0] for t in dados]
tempos_de_chegada = [t[1] for t in dados]

# Configurações iniciais
NUMERO_DE_ATENDENTES = 1  # Um atendente dedicado ao suporte online

# Função para simular o processo de chegada de clientes e atendimento
def processo_cliente(env, servidor, tempos_de_chegada, tempos_de_servico):
    """Função que gera clientes e simula o atendimento."""
    for i in range(len(tempos_de_chegada)):
        # Tempo até a próxima chegada
        yield env.timeout(tempos_de_chegada[i])

        # Iniciar o processo de atendimento do cliente
        env.process(atender_cliente(env, servidor, tempos_de_servico[i]))

def atender_cliente(env, servidor, tempo_de_servico):
    """Processo de atendimento do cliente."""
    with servidor.request() as pedido:
        # Aguardar até o atendente estar disponível
        yield pedido
        # Realizar o atendimento
        yield env.timeout(tempo_de_servico)
        print(f"Cliente atendido em {env.now:.2f} unidades de tempo")

# Configuração do simulador
def simulacao(tempos_de_chegada, tempos_de_servico):
    env = simpy.Environment()
    servidor = simpy.Resource(env, capacity=NUMERO_DE_ATENDENTES)
    
    # Iniciar o processo de clientes
    env.process(processo_cliente(env, servidor, tempos_de_chegada, tempos_de_servico))
    
    # Rodar a simulação
    env.run()

# Executar a simulação
simulacao(tempos_de_chegada, tempos_de_servico)